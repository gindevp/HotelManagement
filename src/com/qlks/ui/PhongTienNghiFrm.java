    private void init() {
        txtSoPhong.setText(soPhong);
        this.fillTbl();
        this.fillCbo();
        this.updateStatus();
    }

    private void fillTbl() {
        DefaultTableModel model = (DefaultTableModel) tbl.getModel();
        model.setRowCount(0);
        List<PhongTienNghi> list = ptndao.selectBySoPhong(soPhong);
        list.forEach((i) -> {
            String tenTN = tndao.selectNameById(i.getMaTienNghi());
            model.addRow(new Object[]{
                i.getId(),
                tenTN,
                i.getSoLuong(),
                i.getTinhTrang()
            });
        });
    }

    private void fillCbo() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbo.getModel();
        model.removeAllElements();
        List<TienNghi> list = tndao.selectAll();
        list.forEach((i) -> {
            model.addElement(i);
        });
    }

    private void updateStatus() {
        boolean edit = this.index > 0;
        btnInsert.setEnabled(!edit);
        btnUpdate.setEnabled(edit);
        btnDelete.setEnabled(edit);
    }

    private void edit() {
        Integer id = Integer.parseInt(tbl.getValueAt(this.index, 0).toString());
        System.out.println(id);
        PhongTienNghi ptn = ptndao.selectByID(id);
        this.setForm(ptn);
        this.updateStatus();
    }

    private PhongTienNghi getForm() {
        PhongTienNghi ptn = null;
        if (Validator.checkBlack(txtTinhTrang) && Validator.checkPosNum(txtSoLuong)) {
            TienNghi tienNghi = (TienNghi) cbo.getSelectedItem();
            ptn = new PhongTienNghi(
                    txtSoPhong.getText(),
                    tienNghi.getMa(),
                    Integer.parseInt(txtSoLuong.getText().trim()),
                    txtTinhTrang.getText().trim()
            );
        }
        return ptn;
    }

    private void setForm(PhongTienNghi ptn) {
        TienNghi tienNghi = tndao.selectByID(ptn.getMaTienNghi());
        cbo.setSelectedItem(tienNghi);
        txtSoLuong.setText(ptn.getSoLuong() + "");
        txtTinhTrang.setText(ptn.getTinhTrang());
    }

