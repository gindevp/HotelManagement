    private void insert() {
        NhaCungCap ncc = getForm();
        if (ncc != null) {
            if (nccdao.insert(ncc)) {
                MsgBox.alert(this, "Thêm thành công!");
                this.fillTable();
                this.clearForm();
            } else {
                MsgBox.alert(null, "Thêm không thành công!");
            }
        }
    }

    private void update() {
        NhaCungCap ncc = getForm();
        Integer id = (Integer) tbl.getValueAt(this.index, 0);
        ncc.setMa(id);
        if (ncc != null) {

            if (nccdao.update(ncc)) {
                MsgBox.alert(this, "Sửa thành công!");
                this.fillTable();
                this.clearForm();
            } else {
                MsgBox.alert(this, "Sửa không thành công!");
            }
        }
    }

    private void delete() {
        if (!com.qlks.util.Auth.isManager()) {
            MsgBox.alert(null, "Không có quyền xóa!");
        } else {
            if (MsgBox.confirm(this, "Có chắc muốn xóa?")) {
                Integer id = (Integer) tbl.getValueAt(this.index, 0);
                if (nccdao.delete(id)) {
                    MsgBox.alert(this, "Xóa thành công!");
                    this.fillTable();
                    this.clearForm();
                } else {
                    MsgBox.alert(this, "Xóa không thành công!");
                }
            }
        }
    }

    private void clearForm() {
        this.index = -1;
        this.setForm(new NhaCungCap());
        txtMa.setText(null);
        this.updateStatus();
        tbl.clearSelection();
    }

    private void first() {
        this.index = 0;
        this.edit();
        tbl.setRowSelectionInterval(this.index, this.index);
    }

    private void prev() {
        if (this.index > 0) {
            this.index--;
            this.edit();
            tbl.setRowSelectionInterval(this.index, this.index);
        }
    }

    private void last() {
        this.index = tbl.getRowCount() - 1;
        this.edit();
        tbl.setRowSelectionInterval(this.index, this.index);

    }

    private void next() {
        if (this.index < tbl.getRowCount() - 1) {
            this.index++;
            this.edit();
            tbl.setRowSelectionInterval(this.index, this.index);
        }
    }

