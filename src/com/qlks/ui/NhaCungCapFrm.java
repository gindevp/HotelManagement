    private void init() {
        this.fillTable();
        this.updateStatus();
        this.sdt();

    }

    private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tbl.getModel();
        model.setRowCount(0);
        List<NhaCungCap> list = nccdao.selectAll();
        list.forEach((item) -> {
            model.addRow(new Object[]{
                item.getMa(),
                item.getTen(),
                item.getDiaChi()
            });
        });
    }

    private void updateStatus() {
        //=true: dang chon 1 hang
        boolean edit = this.index >= 0;
        boolean first = this.index == 0;
        boolean last = this.index == tbl.getRowCount() - 1;

        //-trang thai form
        //neu dang chon 1 dong hien thi len form thi khong duoc sua MANV va k duoc click button Them
        btnInsert.setEnabled(!edit);
        btnUpdate.setEnabled(edit);
        btnDelete.setEnabled(edit);

        //-dieu huong
        // =true khi 1 dong dang duoc click va dong do kp dong dau tien
        txtMa.setEnabled(false);
        btnTienNghi.setEnabled(edit);
        btnFirst.setEnabled(!first);
        btnPre.setEnabled(!first);
        btnNext.setEnabled(!last);
        btnLast.setEnabled(!last);
    }

    private void edit() {
        Integer id = (Integer) tbl.getValueAt(this.index, 0);
        NhaCungCap ncc = nccdao.selectByID(id);
        tbl.setRowSelectionInterval(this.index, this.index);
        this.setForm(ncc);
        this.updateStatus();
    }

    private void setForm(NhaCungCap ncc) {
        txtMa.setText(Integer.toString(ncc.getMa()));
        txtTen.setText(ncc.getTen());
        txtDiaChi.setText(ncc.getDiaChi());
    }

