    private void init() {
        new SplashScreenDialog(this, true).setVisible(true);
        new LoginDialog(this, true).setVisible(true);
        this.setExtendedState(JFrame.MAXIMIZED_BOTH);
        this.showTime();
        this.showName();
    }

    private void showTime() {
        new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                lblThoiGian.setText(timeCurrent());
            }
        }).start();
    }

    private String timeCurrent() {
        SimpleDateFormat sdf = new SimpleDateFormat("hh : mm : ss aa");
        Date date = new Date();
        return sdf.format(date);
    }

    private void showName() {
        Date date = new Date();
        int hour = date.getHours();
        String mess = "";
        String iconPath = "/com/qlks/icon/";
        if (hour >= 1 && hour < 12) {
            mess = "Good morning, ";
            iconPath += "partly_cloudy_day_24px.png";
        } else if (hour >= 12 && hour < 16) {
            mess = "Good afternoon, ";
            iconPath += "sun_24px.png";
        } else if (hour >= 16 && hour < 21) {
            mess = "Good evening, ";
            iconPath += "sunset_24px.png";
        } else {
            mess = "Good night, ";
            iconPath += "night_landscape_24px.png";
        }
        String name = "";
        try {
            name = Auth.user.getTen();
        } catch (Exception e) {
            name = "Some one :)";
        }
        lblName.setText(mess + name);
        lblName.setIcon(new ImageIcon(XImage.class.getResource(iconPath)));
    }

    private void isResize() {
        if (this.getWidth() <= 1200 || this.getHeight() <= 800) {
            this.setVisible(false);
            this.setResizable(false);
            this.setExtendedState(JFrame.MAXIMIZED_BOTH);
            this.setLocationRelativeTo(null);
            this.setVisible(true);
        } else {
            this.setResizable(true);
        }
    }

