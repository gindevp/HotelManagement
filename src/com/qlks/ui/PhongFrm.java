    private void init() {
        this.fillCboLoaiGia();
        this.fillCboLoaiPhong();
        this.fillTable();
        this.updateStatus();
    }

    private void fillCboLoaiPhong() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboLoaiPhong.getModel();
        model.removeAllElements();
        List<LoaiPhong> list = lpdao.selectAll();
        list.forEach((item) -> {
            model.addElement(item);
        });
    }

    private void fillCboLoaiGia() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboLoaiGia.getModel();
        model.removeAllElements();
        List<LoaiGia> list = lgdao.selectAll();
        for (LoaiGia item : list) {
            model.addElement(item);
        }
    }

    private void fillDonGia() {
        try {
            LoaiGia lg = (LoaiGia) cboLoaiGia.getSelectedItem();
            LoaiPhong lp = (LoaiPhong) cboLoaiPhong.getSelectedItem();
            if (lp != null && lg != null) {
                LoaiPhongLoaiGia lplg = lplgdao.selectByLpAndLg(lp.getMa(), lg.getMa());
                lblDonGia.setText(FormatData.formatMoney(lplg.getDonGia()));
            }
        } catch (Exception e) {
        }
    }

    private void fillTable() {
        LoaiPhong lp = (LoaiPhong) cboLoaiPhong.getSelectedItem();
        DefaultTableModel model = (DefaultTableModel) tbl.getModel();
        model.setRowCount(0);

        List<Phong> list = pdao.selectByKeyword(lp.getMa(), txtKeyword.getText().trim());
        list.forEach((item) -> {
            model.addRow(new Object[]{
                item.getSoPhong(),
                item.getSucChua(),
                item.isTrangThai() ? "Đang thuê" : "Trống",
                item.getMaLP()
            });
        }
        );
    }

    private void updateStatus() {
        //=true: dang chon 1 hang
        boolean edit = this.index >= 0;
        boolean first = this.index == 0;
        boolean last = this.index == tbl.getRowCount() - 1;

        //-trang thai form
        //neu dang chon 1 dong hien thi len form thi khong duoc sua MANV va k duoc click button Them
        txtSo.setEditable(!edit);
        btnInsert.setEnabled(!edit);
        btnUpdate.setEnabled(edit);
        btnDelete.setEnabled(edit);

        //-dieu huong
        // =true khi 1 dong dang duoc click va dong do kp dong dau tien
        btnTienNghi.setEnabled(edit);
        btnFirst.setEnabled(edit && !first);
        btnPre.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);

        rdoTrong.setEnabled(edit);
        rdoDangThue.setEnabled(edit);
    }

    private void edit() {
        String ma = tbl.getValueAt(this.index, 0).toString();
        Phong p = pdao.selectByID(ma);
        tbl.setRowSelectionInterval(this.index, this.index);
        this.setForm(p);
        this.updateStatus();
        tbl.setRowSelectionInterval(this.index, this.index);
    }

    private void setForm(Phong p) {
        txtSo.setText(p.getSoPhong());
        txtSucChua.setText(Integer.toString(p.getSucChua()));
        rdoTrong.setSelected(!p.isTrangThai());
        rdoDangThue.setSelected(p.isTrangThai());
        LoaiPhong loaiPhong = lpdao.selectByID(p.getMaLP());
        if (loaiPhong != null) {
            cboLoaiPhong.setSelectedItem(loaiPhong);
        }
    }

    private Phong getForm() {
        Phong p = null;
        LoaiPhong lp = (LoaiPhong) cboLoaiPhong.getSelectedItem();
        String[] title = new String[]{"Số phòng", "Sức chứa"};
        String[] titleNum = new String[]{"Sức chứa"};
        if (Validator.checkBlack(this, title, txtSo, txtSucChua)
                && Validator.checkPosNumNotIsZero(this, titleNum, txtSucChua)) {
            p = new Phong(
                    txtSo.getText().trim(),
                    Integer.parseInt(txtSucChua.getText().trim()),
                    rdoTrong.isSelected() ? false : true,
                    lp.getMa()
            );
        }
        return p;
    }

    private void insert() {
        Phong p = getForm();
        if (p != null) {
            if (pdao.insert(p)) {
                MsgBox.alert(this, "Thêm thành công!");
                this.clearForm();
                this.fillTable();
            } else {
                MsgBox.alert(this, "Thêm không thành công! Phòng đã tồn tại");
            }
        }
    }

    private void update() {
        Phong p = getForm();
        if (p != null) {
            if (pdao.update(p)) {
                MsgBox.alert(null, "Sửa thành công!");
                this.fillTable();
                this.clearForm();
            } else {
                MsgBox.alert(null, "Sửa không thành công!");
            }
        }
    }

    private void delete() {
        if (!com.qlks.util.Auth.isManager()) {
            MsgBox.alert(null, "Không có quyền xóa!");
        } else {
            if (MsgBox.confirm(null, "Có chắc muốn xóa?")) {
                String ma = tbl.getValueAt(this.index, 0).toString();
                if (pdao.delete(ma)) {
                    MsgBox.alert(null, "Xóa thành công!");
                    this.fillTable();
                    this.clearForm();
                } else {
                    MsgBox.alert(null, "Xóa không thành công!");
                }
            }
        }
    }

    private void clearForm() {
        this.index = -1;
        this.setForm(new Phong());
        this.updateStatus();
        tbl.clearSelection();
    }

    private void first() {
        this.index = 0;
        this.edit();
    }

    private void prev() {
        if (this.index > 0) {
            this.index--;
            this.edit();
        }
    }

    private void last() {
        this.index = tbl.getRowCount() - 1;
        this.edit();
    }

    private void next() {
        if (this.index < tbl.getRowCount() - 1) {
            this.index++;
            this.edit();
        }
    }

