    private void fillTblHoaDon() {
        DefaultTableModel model = (DefaultTableModel) tblHoaDon.getModel();
        model.setRowCount(0);
        List<Object[]> list = tkdao.getHoaDon();
        int i = 1;
        for (Object[] obj : list) {
            model.addRow(new Object[]{
                i,
                obj[0],
                obj[1],
                obj[2],
                FormatData.formatMoney(Double.parseDouble(obj[3].toString())),
                obj[4],
                obj[5],
                obj[6]
            });
            i++;
        }
    }

