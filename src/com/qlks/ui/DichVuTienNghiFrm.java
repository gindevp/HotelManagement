    //Dich vu methods
    private void fillTableDV() {
        DefaultTableModel model = (DefaultTableModel) tblDV.getModel();
        model.setRowCount(0);
        List<DichVu> list = dvdao.selectAll();
        list.forEach((i) -> {
            model.addRow(new Object[]{
                i.getMa(),
                i.getTen(),
                FormatData.formatMoney(i.getGia()),
                i.getMoTa(),
                i.isTrangThai() ? "Sẵn sàng" : "Tạm thời ngừng"
            });
        });
    }

    private void updateStatusDV() {
        //=true: dang chon 1 hang
        boolean edit = this.indexDV >= 0;
        boolean first = this.indexDV == 0;
        boolean last = this.indexDV == tblDV.getRowCount() - 1;

        //-trang thai form
        //neu dang chon 1 dong hien thi len form thi khong duoc sua MANV va k duoc click button Them
        btnInsertDV.setEnabled(!edit);
        btnUpdateDV.setEnabled(edit);
        btnDeleteDV.setEnabled(edit);

        //-dieu huong
        // =true khi 1 dong dang duoc click va dong do kp dong dau tien
        btnFirstDV.setEnabled(edit && !first);
        btnPreDV.setEnabled(edit && !first);
        btnNextDV.setEnabled(edit && !last);
        btnLastDV.setEnabled(edit && !last);
    }

    private void editDV() {
        Integer id = Integer.parseInt(tblDV.getValueAt(this.indexDV, 0).toString());
        DichVu dv = dvdao.selectByID(id);
        this.setFormDV(dv);
        tblDV.setRowSelectionInterval(this.indexDV, this.indexDV);
        this.updateStatusDV();
    }

    private void setFormDV(DichVu dv) {
        txtTenDV.setText(dv.getTen());
        txtDonGia.setText(FormatData.formatMoney(dv.getGia()));
        txtMoTa.setText(dv.getMoTa());
        rdo1.setSelected(dv.isTrangThai());
        rdo0.setSelected(!dv.isTrangThai());
    }

    private DichVu getFormDV() {
        DichVu dv = null;
        String[] title = new String[]{"Tên dịch vụ", "Đơn giá", "Mô tả"};
        String[] titleNum = new String[]{"Đon giá"};
        if (Validator.checkBlack(this, title, txtTenDV, txtDonGia, txtMoTa)
                && Validator.checkPosNum(this, titleNum, txtDonGia)) {
            String ten = txtTenDV.getText().trim();
            double gia = Double.parseDouble(txtDonGia.getText());
            String moTa = txtMoTa.getText().trim();
            boolean trangThai = rdo1.isSelected() ? true : false;

            dv = new DichVu(ten, gia, moTa, trangThai);
        }
        return dv;
    }

    private void insertDV() {
        DichVu dv = this.getFormDV();
        if (dv != null) {
            if (dvdao.insert(dv)) {
                MsgBox.alert(this, "Thêm thành công!");
                this.clearFormDV();
                this.fillTableDV();
            } else {
                MsgBox.alert(this, "Thêm không thành công!");
            }
        }
    }

    private void updateDV() {
        DichVu dv = this.getFormDV();
        if (dv != null) {
            Integer id = Integer.parseInt(tblDV.getValueAt(this.indexDV, 0).toString());
            dv.setMa(id);
            if (dvdao.update(dv)) {
                MsgBox.alert(null, "Sửa thành công!");
                this.fillTableDV();
                this.clearFormDV();
            } else {
                MsgBox.alert(null, "Sửa không thành công!");
            }
        }
    }

    private void deleteDV() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "Không có quyên xóa!");
        } else {
            if (MsgBox.confirm(this, "Bạn có chắc muốn xóa?")) {
                Integer ma = Integer.parseInt(tblDV.getValueAt(this.indexDV, 0).toString());
                if (dvdao.delete(ma)) {
                    MsgBox.alert(this, "Xóa thành công!");
                    this.fillTableDV();
                    this.clearFormDV();
                } else {
                    MsgBox.alert(this, "Xóa không thành công!");
                }
            }
        }
    }

    private void clearFormDV() {
        this.indexDV = -1;
        this.setFormDV(new DichVu());
        this.updateStatusDV();
        tblDV.clearSelection();
    }

    private void firstDV() {
        this.indexDV = 0;
        this.editDV();
    }

    private void prevDV() {
        if (this.indexDV > 0) {
            this.indexDV--;
            this.editDV();
        }
    }

    private void nextDV() {
        if (this.indexDV < tblDV.getRowCount() - 1) {
            this.indexDV++;
            this.editDV();
        }
    }

    private void lastDV() {
        this.indexDV = tblDV.getRowCount() - 1;
        this.editDV();
    }

